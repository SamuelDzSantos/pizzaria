        private void setClienteValues(Cliente cliente) {
                if (cliente != null) {
                        this.nome.setText(cliente.getNome());
                        this.sobrenome.setText(cliente.getSobrenome());
                        this.telefone.setText(cliente.getTelefone());
                        this.endereco.setText(cliente.getEndereco());
                        this.cliente = cliente;
                }
        }

        private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btSalvarActionPerformed

                JFrame frame = new JFrame("frame");
                String[] listaDeCampos = { "nome", "sobrenome", "telefone", "endereco" };
                if (stringEmpty(frame, listaDeCampos, nome.getText(), sobrenome.getText(), telefone.getText(),
                                endereco.getText())) {
                        return;
                }
                if (this.cliente == null) {
                        this.clienteController.addCliente(nome.getText(), sobrenome.getText(), telefone.getText(),
                                        endereco.getText());
                } else {
                        this.clienteController.updateCliente(cliente.getId(), nome.getText(), sobrenome.getText(),
                                        telefone.getText(),
                                        endereco.getText());
                }
                this.dispose();
        }

        // GEN-LAST:event_btSalvarActionPerformed

        private boolean stringEmpty(JFrame frame, String[] campos, String... strings) {
                Boolean value = false;
                int x = 0;
                for (String string : strings) {
                        if (string.isEmpty()) {
                                JOptionPane.showMessageDialog(frame, "Por favor, preencha o campo " + campos[x] + ".");
                                value = true;
                        }
                        x++;
                }
                return value;
        }

